def _add_kakao_parser(subparsers: _SubparserGroup) -> None:
    kakao = subparsers.add_parser("kakao")
    sub = kakao.add_subparsers(dest="kakao_command", required=True)
    login = sub.add_parser("login")
    _ = login.add_argument("--headed", action="store_true")
    _ = login.add_argument("--admin-url")
    _ = login.add_argument("--json", action="store_true")
    _add_profile_args(login)
    calibrate = sub.add_parser("calibrate")
    _ = calibrate.add_argument("--json", action="store_true")
    _add_profile_args(calibrate)
    status = sub.add_parser("status")
    _ = status.add_argument("--json", action="store_true")
    _add_profile_args(status)
    send = sub.add_parser("send-friend")
    _ = send.add_argument("--message")
    _ = send.add_argument("--body-file")
    _ = send.add_argument("--image")
    _ = send.add_argument("--group")
    _ = send.add_argument("--approval-id")
    _ = send.add_argument("--max")
    _ = send.add_argument("--json", action="store_true")
    _add_profile_args(send)
    chats = sub.add_parser("chats")
    chat_sub = chats.add_subparsers(dest="kakao_chats_command", required=True)
    pull = chat_sub.add_parser("pull")
    _ = pull.add_argument("--admin-url")
    _ = pull.add_argument("--headed", action="store_true")
    _ = pull.add_argument("--json", action="store_true")
    _add_profile_args(pull)
    reply = chat_sub.add_parser("reply")
    _ = reply.add_argument("--contact", required=True)
    _ = reply.add_argument("--message", required=True)
    _ = reply.add_argument("--approval-id")
    _ = reply.add_argument("--admin-url")
    _ = reply.add_argument("--headed", action="store_true")
    _ = reply.add_argument("--json", action="store_true")
    _add_profile_args(reply)
    for name in ("history", "summary"):
        command = sub.add_parser(name)
        _ = command.add_argument("--contact", required=True)
        _ = command.add_argument("--json", action="store_true")
        _add_profile_args(command)


def _add_profile_args(parser: argparse.ArgumentParser) -> None:
    _ = parser.add_argument("--profile-root")
    _ = parser.add_argument("--profile")
