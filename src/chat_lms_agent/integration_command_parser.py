from __future__ import annotations

from typing import TYPE_CHECKING, Protocol

if TYPE_CHECKING:
    import argparse


class _SubparserGroup(Protocol):
    def add_parser(self, name: str) -> argparse.ArgumentParser: ...


def add_integration_parsers(subparsers: _SubparserGroup) -> None:
    _add_classcard_parser(subparsers)
    _add_gws_parser(subparsers)
    _add_kakao_parser(subparsers)


def _add_gws_parser(subparsers: _SubparserGroup) -> None:
    gws = subparsers.add_parser("gws")
    sub = gws.add_subparsers(dest="gws_command", required=True)
    setup = sub.add_parser("setup")
    _ = setup.add_argument("--client-file")
    _ = setup.add_argument("--token-file")
    _ = setup.add_argument("--json", action="store_true")
    status = sub.add_parser("status")
    _ = status.add_argument("--token-file")
    _ = status.add_argument("--json", action="store_true")
    client = sub.add_parser("client")
    _ = client.add_argument("client_verb", choices=["install"])
    _ = client.add_argument("--downloads")
    _ = client.add_argument("--to")
    _ = client.add_argument("--json", action="store_true")
    calendar = sub.add_parser("calendar")
    _ = calendar.add_argument("calendar_verb", choices=["list", "create-event"])
    _ = calendar.add_argument("--from", dest="time_from")
    _ = calendar.add_argument("--to", dest="time_to")
    _ = calendar.add_argument("--title")
    _ = calendar.add_argument("--start")
    _ = calendar.add_argument("--end")
    _ = calendar.add_argument("--description")
    _ = calendar.add_argument("--token-file")
    _ = calendar.add_argument("--json", action="store_true")
    drive = sub.add_parser("drive")
    _ = drive.add_argument("drive_verb", choices=["upload"])
    _ = drive.add_argument("--file", required=True)
    _ = drive.add_argument("--folder-name")
    _ = drive.add_argument("--token-file")
    _ = drive.add_argument("--json", action="store_true")
    sheets = sub.add_parser("sheets")
    _ = sheets.add_argument(
        "sheets_verb",
        choices=[
            "create",
            "append",
            "update",
            "clear",
            "batch-update",
            "batch-clear",
            "values-get",
        ],
    )
    _ = sheets.add_argument("--title")
    _ = sheets.add_argument("--sheet-id")
    _ = sheets.add_argument("--range")
    _ = sheets.add_argument("--from-tsv")
    _ = sheets.add_argument("--from-json")
    _ = sheets.add_argument("--out")
    _ = sheets.add_argument("--token-file")
    _ = sheets.add_argument("--json", action="store_true")
    gmail = sub.add_parser("gmail")
    _ = gmail.add_argument("gmail_verb", choices=["send"])
    _ = gmail.add_argument("--to", required=True)
    _ = gmail.add_argument("--subject", required=True)
    _ = gmail.add_argument("--body-file", required=True)
    _ = gmail.add_argument("--attach")
    _ = gmail.add_argument("--approval-id")
    _ = gmail.add_argument("--token-file")
    _ = gmail.add_argument("--json", action="store_true")
    _add_profile_args(gmail)


